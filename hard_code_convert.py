def print_ascii_art(text):
    text = text.upper()
    rows = [""] * 5  # assuming all characters are 5 rows tall

    for char in text:
        if char in ascii_art:
            for i in range(5):
                rows[i] += ascii_art[char][i] + "  "
        else:
            for i in range(5):
                rows[i] += "     "  # placeholder for unknown chars

    for row in rows:
        print(row)
        
        

